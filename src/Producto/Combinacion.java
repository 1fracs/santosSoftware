public class Combinacion {
    private int idCombinacion;
    private int claveles;
    private int margaritas;
    private int gladiolos;
    private int rosas;
    private int gerberas;
    private int verdes;
    private int lilium;
    private int alstromeria;
    private int anturium;
    private int lisiantus;
    private int paniculata;
    private int ruscus;

    public Combinacion(int idCombinacion, int claveles, int margaritas, int gladiolos, int rosas, int gerberas, int verdes, int lilium, int alstromeria, int anturium, int lisiantus, int paniculata, int ruscus) {
        this.idCombinacion = idCombinacion;
        this.claveles = claveles;
        this.margaritas = margaritas;
        this.gladiolos = gladiolos;
        this.rosas = rosas;
        this.gerberas = gerberas;
        this.verdes = verdes;
        this.lilium = lilium;
        this.alstromeria = alstromeria;
        this.anturium = anturium;
        this.lisiantus = lisiantus;
        this.paniculata = paniculata;
        this.ruscus = ruscus;
    }

    public int getIdCombinacion() {
        return idCombinacion;
    }

    public void setIdCombinacion(int idCombinacion) {
        this.idCombinacion = idCombinacion;
    }

    public int getClaveles() {
        return claveles;
    }

    public void setClaveles(int claveles) {
        this.claveles = claveles;
    }

    public int getMargaritas() {
        return margaritas;
    }

    public void setMargaritas(int margaritas) {
        this.margaritas = margaritas;
    }

    public int getGladiolos() {
        return gladiolos;
    }

    public void setGladiolos(int gladiolos) {
        this.gladiolos = gladiolos;
    }

    public int getRosas() {
        return rosas;
    }

    public void setRosas(int rosas) {
        this.rosas = rosas;
    }

    public int getGerberas() {
        return gerberas;
    }

    public void setGerberas(int gerberas) {
        this.gerberas = gerberas;
    }

    public int getVerdes() {
        return verdes;
    }

    public void setVerdes(int verdes) {
        this.verdes = verdes;
    }

    public int getLilium() {
        return lilium;
    }

    public void setLilium(int lilium) {
        this.lilium = lilium;
    }

    public int getAlstromeria() {
        return alstromeria;
    }

    public void setAlstromeria(int alstromeria) {
        this.alstromeria = alstromeria;
    }

    public int getAnturium() {
        return anturium;
    }

    public void setAnturium(int anturium) {
        this.anturium = anturium;
    }

    public int getLisiantus() {
        return lisiantus;
    }

    public void setLisiantus(int lisiantus) {
        this.lisiantus = lisiantus;
    }

    public int getPaniculata() {
        return paniculata;
    }

    public void setPaniculata(int paniculata) {
        this.paniculata = paniculata;
    }

    public int getRuscus() {
        return ruscus;
    }

    public void setRuscus(int ruscus) {
        this.ruscus = ruscus;
    }
    
    
}
